    public void trainname() {

        try {
            ps = con.prepareStatement("SELECT DISTINCT trainName FROM `train_info`;");
            rs = ps.executeQuery();
            tname.removeAllItems();

            while (rs.next()) {
                tname.addItem(rs.getString(1));
            }

        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

